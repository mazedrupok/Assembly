TITLE EX4:DISPLAY TWO LINE OF MESSAGE IN DIFFERENT LINE
.MODEL SMALL 
.STACK 100H
.DATA 
	MSG1 DB 'CSTE$'
	MSG2 DB 0DH,0AH,'NSTU$'
.CODE
	MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	LEA DX,MSG1
	MOV AH,09H
	INT 21H

	LEA DX,MSG2
	MOV AH,09H
	INT 21H
	
	MOV AH,4CH
	INT 21H

	MAIN ENDP
		END MAIN
	